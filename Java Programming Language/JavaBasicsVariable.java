public class JavaBasicsVariable {
    public static void main(String args[]){
        int a = 10;
        int b = 2;

        System.out.println(a);
        System.out.println(b);

        String name = "Shaktimaan";
        System.out.println(name);

        a = 64;
        System.out.println(a);

        a = b;
        System.out.println(a);
    }
}
